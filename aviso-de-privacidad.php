                OBJETO DEL AVISO.
            </p>
            <p class="txtA_2">
                El presente aviso tiene por objeto la protección de sus datos personales, mediante su tratamiento legítimo, controlado e informado, a efecto de garantizar su privacidad, así como su derecho a la autodeterminación informativa.
            </p>


            <p class="titulosA">
                IDENTIDAD Y DOMICILIO DEL RESPONSABLE.
            </p>
            <p class="txtA_2">
                Grupo Administrativo Mexicano, S.A. de C.V. (en adelante “GAM”) con domicilio en Avenida Paseo de la Reforma No. 144, 6° Piso, Colonia Juárez, Código Postal 06600, Alcaldía Cuauhtémoc, Ciudad de México, será responsable del tratamiento de sus datos personales que sean recabados y/o proporcionados directamente por usted a través de cuestionarios, formularios, solicitudes, de los agentes de seguros que colaboran con esta empresa y Grupo Nacional Provincial, S.A.B. (GNP), de terceros autorizados por GAM o por vía electrónica, óptica, sonora, visual o por cualquier otro medio o tecnología, o de otras fuentes permitidas por la Ley o los que se generen de estas o de la relación que lleguemos a establecer, así como de su uso y protección, por lo que se compromete a guardar estricta confidencialidad, así como a llevar a cabo esfuerzos razonables para su protección.
            </p>

            <p class="titulosA">
                FINALIDADES DEL TRATAMIENTO DE DATOS PERSONALES.
            </p>
            <p class="txtA_2">
                GAM hace de su conocimiento que es necesario recabar sus datos personales, mismos que tratará de acuerdo con las siguientes finalidades:
            </p>

            <ol>
                <li class="txtA_sec"> Primarias </li>
            </ol>

            <p class="txtA_2">
            <ul>
                <li>Para crear y administrar el expediente del agente, así como digitalizar y archivar la información que lo conforma;</li>
                <li>Para reunir y validar la información proporcionada a través de otras fuentes o medios para asegurarse que la información es correcta y completa;</li>
                <li>Para atender los requerimientos solicitados por las autoridades competentes que, en el ámbito de su control y especialidad así lo requieran;</li>
                <li>Para llevar a cabo nuestras actividades de auditoría interna, externa y/o por autoridades competentes;</li>
                <li>Para mantener comunicación con usted a través de los datos que proporcione con relación a su incorporación como agente, o la que corresponda y según aplique de acuerdo a la relación de GAM o GNP con el contratante del que usted forme parte;</li>
                <li>Para realizar y/o brindarle contratación, inducción, capacitación, desarrollo, promoción, actualización de datos y notificaciones; </li>
                <li>Para realizar todas y cada una de las actividades propias para mantener la relación existente entre usted y GAM, o las que correspondan y según aplique de acuerdo la relación de GAM con el contratante del que usted forme parte;</li>
                <li>Mantener comunicación con usted a través de sus redes sociales para brindar atención y/o seguimiento personalizado con fundamento en la relación jurídica directa entre usted y GAM o GNP o alguna existente de la que forme parte;</li>
                <li>En su caso, gestionar el termino o conclusión de la relación existente con GAM, o lo que corresponda y según aplique de acuerdo a la relación de GAM con el contratante del que usted forma parte;</li>
                <li>Para llevar a cabo la evaluación del desempeño de sus actividades; así como, en su caso, logros obtenidos; </li>
                <li>Recibir y gestionar las solicitudes para el ejercicio de los Derechos de los Titulares;</li>
                <li>Para gestionar y verificar el uso correcto de las herramientas proporcionadas por GAM, tales como, sistemas computacionales, redes, Internet, etc.; así como la confidencialidad de la información propiedad de GAM;</li>
                <li>Para dar cumplimiento a las obligaciones contraídas con usted.</li>
            </ul>
            </p>

            <ol start="2">
                <li class="txtA_sec"> Secundarias </li>
            </ol>

            <p class="txtA_2">
