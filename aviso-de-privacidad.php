                <li>Invitarlo a usted, familiares o terceros a participar en eventos, concursos y actividades organizadas por la empresa;</li>
                <li>Invitarlo a participar en estudios de mercado para conocer su opinión sobre sitios web, plataformas digitales o aplicaciones que tienen como propósito verificar la posibilidad de mejorar cualquiera de sus elementos, para su posterior uso en una versión productiva;</li>
                <li>Encuestas de opinión, estudios de mercado y/o encuestas relacionadas con la empresa.</li>
            </ul>
            </p>

            <p class="txtA_2">
                Si usted no está de acuerdo con las finalidades secundarias, podrá manifestar su negativa siguiendo los mecanismos de la página <a target="_blank" href="https://www.asesoresgam.com.mx">www.asesoresgam.com.mx</a> en el apartado Avisos de Privacidad, según corresponda.
            </p>

            <p class="titulosA">
                DATOS PERSONALES TRATADOS
            </p>

            <p class="txtA_2">
                Para cumplir con las finalidades del tratamiento señaladas en el presente Aviso de Privacidad, es necesario recabar y dar tratamiento a las siguientes categorías de datos personales:
            </p>

            <p class="txtA_3">
            <ul>
                <li>Datos de identificación (nombre, edad, fecha de nacimiento, etc.);</li>
                <li>Datos de contacto (teléfono, correo electrónico, etc.);</li>
                <li>Datos de ubicación (dirección, etc.);</li>
                <li>Datos laborales (experiencia y antecedentes laborales, referencias laborales, etc.);</li>
                <li>Datos patrimoniales (situación socioeconómica, datos de la cuenta bancaria a la que se realizaran diversos depósitos, etc.);</li>
                <li>Datos académicos (experiencia y antecedentes académicos escolaridad, profesión, etc.);</li>
                <li>Datos de terceros (contacto de emergencia, etc.);</li>
                <li>Datos biométricos.</li>
            </ul>
            </p>

            <p class="txtA_2">
                Adicionalmente, en ciertos casos, podemos requerir los siguientes datos: estado de salud presente y futuro, información genética, asimismo, obtendremos datos que le permitirán compartirnos sus opiniones, ideas o percepciones, mismos que son considerados por la Ley como datos personales sensibles; ante tal situación sus datos serán tratados bajo las más estrictas medidas de seguridad que garanticen su confidencialidad.
            </p>

            <p class="titulosA">
                TRANSFERENCIA DE DATOS PERSONALES
            </p>

            <p class="txtA_2">
                GAM podrá transferir sus datos personales a entidades nacionales para las finalidades arriba mencionadas; por lo que, mediante el simple uso de esta página, sus aplicaciones, o la celebración de un contrato con GAM, usted acepta la transferencia de sus datos. Si usted no manifiesta su oposición por escrito al correo electrónico <a>contacto@asesoresgam.com.mx</a> para que sus datos personales sean transferidos, se entenderá que ha otorgado su consentimiento para ello.
            </p>

            <p class="txtA_3">
                Los datos a que se refiere este Aviso podrán ser transferidos a:
            </p>

            <ol type="a">
                <li>Sociedades relacionadas o subsidiarias, con la finalidad de ofrecerle otros productos y servicios;</li>
                <li>Autoridades competentes, con la finalidad de dar cumplimiento a nuestras obligaciones legales;</li>
                <li>Autoridades judiciales, con la finalidad de dar cumplimiento a notificaciones, requerimientos u oficios de carácter judicial;</li>
                <li>Instituciones, organizaciones o entidades del sector asegurador, para selección de riesgos, contratación, desarrollo;</li>
                <li>Nuestras sucursales y afiliados comerciales con las que mantengamos una relación jurídica y/o sean nuestros socios comerciales, que operan bajo las mismas políticas internas en materia de protección de datos personales; para los fines previstos en el presente Aviso de Privacidad.</li>
            </ol>

            <p class="txtA_2">
                Conforme a lo establecido por la Ley, las transferencias nacionales o internacionales de datos podrán llevarse a cabo sin el consentimiento del titular en los siguientes cuando:
            </p>

            <ol type="I">
                <li>La trasferencia esté prevista en una Ley o Tratado en los que México sea parte;</li>
                <li>La transferencia sea necesaria para la prevención o el diagnóstico médico, la prestación de asistencia sanitaria, tratamiento médico o la gestión de servicios sanitarios;</li>
                <li>La transferencia sea efectuada a sociedades controladoras, subsidiarias o afiliadas bajo el control común del responsable, o a una sociedad matriz o a cualquier sociedad del mismo grupo del responsable que opere bajo los mismos procesos y políticas internas.</li>
                <li>La transferencia sea necesaria por virtud de un contrato celebrado o por celebrar en interés del titular, por el responsable y un tercero.</li>
                <li>La transferencia sea necesaria o legalmente exigida para la salvaguarda de un interés público o para la procuración o administración de justicia.</li>
                <li>La transferencia sea precisa para el reconocimiento, ejercicio o defensa de un derecho en un proceso judicial.</li>
                <li>La transferencia sea precisa para el mantenimiento o cumplimiento de una relación jurídica entre el responsable y el titular, los demás casos que establezca la Ley.</li>
            </ol>

            <p class="txtA_2">
                No obstante, sus datos son custodiados y manejados cuidando que no tengan un uso diferente al señalado con anterioridad, guardándose la debida confidencialidad de los mismos frente a terceros que no tengan ninguna facultad para acceder a dicha información.
                <br><br>
                En caso de que realicemos alguna transferencia adicional que requiera su consentimiento expreso, se recabará el mismo previamente.
            </p>


            <p class="titulosA">
                DERECHOS DE ACCESO, RECTIFICACIÓN, CANCELACIÓN, OPOSICIÓN (ARCO) Y RENOVACIÓN.
            </p>
            <p class="txtA_2">
                Usted o su representante legal podrá acceder a los datos personales que poseemos y al detalle del tratamiento de los mismos, así como a rectificarlos en caso de ser inexactos o incompletos; cancelarlos cuando considere que no se requieren para alguna de las finalidades señaladas en el presente Aviso de Privacidad, estén siendo utilizados para finalidades no consentidas o haya finalizado la relación contractual o de servicio, oponerse al tratamiento de los mismos, o bien, revocar el consentimiento que para tal fin nos haya otorgado.
            </p>

            <p class="txtA_3">
                Para ejercer sus ARCO, así como para limitar el uso o divulgación de datos personales o revocar su consentimiento para el tratamiento de los mismos deberá seguir el siguiente proceso:
            </p>


            <p class="txtA_3">
            <ol type="1">
                <li>Usted o su representante legal deben presentar su petición por escrito directamente en nuestras oficinas o enviarla al correo electrónico <a>contacto@asesoresgam.com.mx</a> a través del <a>Formato de Solicitud de Derechos ARCO</a>, dicho formato se encuentra disponible en el Aviso de Privacidad de nuestro sitio web <a target="_blank" href="http://www.asesoresgam.com.mx">http://www.asesoresgam.com.mx.</a></li>
            </ol>
            </p>

            <p class="txtA_2">
                En cumplimiento al artículo 29 de la Ley, dicha solicitud deberá contener los siguientes datos:
            </p>

            <p class="txtA_3">
            <ol type="a">
                <li>Su nombre y domicilio u otro medio para comunicarle la respuesta a su solicitud;</li>
                <li>Los documentos que acrediten su identidad o, en su caso, la representación legal del titular;</li>
                <li>La descripción clara y precisa de los datos personales respecto de los que se busca ejercer alguno de los derechos mencionados anteriormente, y</li>
                <li>Cualquier otro elemento o documento que facilite la localización de sus datos personales.</li>
            </ol>
            </p>

            <p class="txtA_2">
            <ol start="2">
                <li>GAM debe recibir y confirmar la recepción de su solicitud para poder evaluarla.</li>
            </ol>
            </p>

            <p class="txtA_2">
            <ol start="3">
                <li>GAM debe comunicar la respuesta y determinación adoptada a su solicitud en un plazo máximo de veinte días hábiles, contados desde la fecha en que se recibió.</li>
            </ol>
            </p>

            <p class="txtA_2">
            <ol type="a">
                <li>En caso de que por alguna causa se determine negar el derecho solicitado, se debe comunicar el motivo de la decisión, acompañándola de las pruebas que resulten pertinentes, en los plazos establecidos.</li>
                <li>GAM dispondrá de un plazo máximo de hasta quince días hábiles posteriores a la fecha en que se comunicó la resolución para llevar a cabo las medidas adoptadas para cumplir con su solicitud en caso de resultar procedente.</li>
            </ol>
            </p>

            <p class="txtA_2">
                Los plazos antes referidos podrán ser ampliados una sola vez por un periodo igual, siempre y cuando así lo justifiquen las circunstancias del caso.
                En caso de solicitar la rectificación de datos personales, adicionalmente deberá indicar las modificaciones a realizarse y aportar la documentación que sustente tu petición.
            </p>

            <p class="titulosA">
                LIMITACIÓN DE USO Y DIVULGACIÓN DE DATOS PERSONALES.
            </p>

            <p class="txtA_2">
                Con objeto de que usted pueda ejercer su derecho de limitar el uso o divulgación de sus datos personales, ponemos a su disposición el mismo procedimiento utilizado para ejercer los derechos ARCO, el cual esta señalado en el apartado anterior.
            </p>

            <p class="titulosA">
                USO DE COOKIES.
            </p>

            <p class="txtA_2">
                GAM en su sitio web podrá utilizar “cookies” para grabar la información de la sesión sobre información específica de las qué páginas visitó el usuario, pero dicha información es utilizada sobre una base anónima y agregada de tal manera que no podrá ser identificado en ella.
                <br><br>
                Los sitios pueden permitir emitir publicidad o la ejecución de funciones de terceros que envían "cookies" al equipo de cómputo de los usuarios. Mediante el uso de éstas "cookies" no se identifica personalmente a los usuarios, únicamente a su equipo de cómputo.
                <br><br>
                Usted podrá cambiar sus opciones a través de sus equipos de cómputo y/o navegador de Internet para dejar de aceptar "cookies" o bien confirmar si las permiten o rechazan. Sin embargo, si desactiva las “cookies” puede ocasionar, no tener acceso a algunas de las funciones personalizadas de nuestro sitio web o impedir el buen funcionamiento de este.
            </p>

            <p class="titulosA">
                MODIFICACIONES AL AVISO DE PRIVACIDAD.
            </p>

            <p class="txtA_2">
                GAM se reserva el derecho, bajo su exclusiva discreción, de cambiar, modificar, agregar o eliminar partes del presente Aviso de Privacidad en cualquier momento. En tal caso, GAM lo publicara de manera generalizada por los medios de comunicación que utilicemos con usted, además estarán a su disposición en nuestro sitio web <a target="_blank" href="http://www.asesoresgam.com.mx">http://www.asesoresgam.com.mx</a> y colocado en nuestras oficinas.
            </p>

            <p class="titulosA">
                LEGISLACIÓN APLICABLE.
            </p>

            <p class="txtA_2">
                La interpretación y aplicación del presente Aviso de Privacidad se regirá por la Ley Federal de Protección de Datos Personales en Posesión de los Particulares, y las demás leyes y disposiciones reglamentarias y administrativas que resulten aplicables.
            </p>

            <p class="titulosA">
                VIGENCIA DEL AVISO.
            </p>

            <p class="txtA_2">
                El presente Aviso de Privacidad comenzará su vigencia a partir del 6 de julio de 2011. Fecha de la última actualización 12 de octubre de 2021.
            </p>

