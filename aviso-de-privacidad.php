            </p>

            <p class="txtA_3">
            <ul>
                <li>Con fines de marketing (mercadotecnia, publicidad o prospección comercial);</li>
                <li>Con fines de evaluar la calidad del servicio y realizar estudios estadísticos; </li>
                <li>Contactar a sus familiares y/o terceros, nombrados para contacto en caso de emergencia, con la misma finalidad;</li>
